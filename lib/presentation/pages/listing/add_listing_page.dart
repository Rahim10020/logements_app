/// Provider pour l'étape actuelle
final currentStepProvider = StateProvider<int>((ref) => 0);

/// Provider pour les données du formulaire
final listingFormProvider = StateProvider<Map<String, dynamic>>((ref) => {});

/// Page d'ajout d'annonce avec formulaire en 6 étapes
class AddListingPage extends ConsumerStatefulWidget {
  const AddListingPage({super.key});

  @override
  ConsumerState<AddListingPage> createState() => _AddListingPageState();
}

class _AddListingPageState extends ConsumerState<AddListingPage> {
  final _formKeys = List.generate(6, (_) => GlobalKey<FormState>());
  final _pageController = PageController();

  @override
  void dispose() {
    _pageController.dispose();
    super.dispose();
  }

  void _nextStep() {
    final currentStep = ref.read(currentStepProvider);
    if (_formKeys[currentStep].currentState?.validate() ?? false) {
      _formKeys[currentStep].currentState?.save();
      if (currentStep < 5) {
        ref.read(currentStepProvider.notifier).state++;
        _pageController.nextPage(
          duration: const Duration(milliseconds: 300),
          curve: Curves.easeInOut,
        );
      } else {
        _submitListing();
      }
    }
  }

  void _previousStep() {
    final currentStep = ref.read(currentStepProvider);
    if (currentStep > 0) {
      ref.read(currentStepProvider.notifier).state--;
      _pageController.previousPage(
        duration: const Duration(milliseconds: 300),
        curve: Curves.easeInOut,
      );
    }
  }

  Future<void> _submitListing() async {
    final formData = ref.read(listingFormProvider);
    final user = ref.read(authStateProvider).value;

    if (user == null) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('Vous devez être connecté')),
      );
      return;
    }

    // Afficher un indicateur de chargement
    showDialog(
      context: context,
      barrierDismissible: false,
      builder: (context) => const Center(child: CircularProgressIndicator()),
    );

    try {
      // Upload images to Firebase Storage
      final imageUrls = <String>[];
      if (formData['images'] != null) {
        final images = formData['images'] as List<String>;
        for (var i = 0; i < images.length; i++) {
          final imagePath = images[i];
          final storageRef = FirebaseStorage.instance
              .ref()
              .child('listings')
              .child(user.uid)
              .child('${DateTime.now().millisecondsSinceEpoch}_$i.jpg');

          await storageRef.putFile(File(imagePath));
          final url = await storageRef.getDownloadURL();
          imageUrls.add(url);
        }
      }

      // Create listing model
      final listing = ListingModel(
        id: '', // Will be generated by Firebase
        title: formData['title'] ?? '',
        description: formData['description'] ?? '',
        type: formData['type'] ?? ListingType.apartment,
        price: (formData['price'] ?? 0).toDouble(),
        city: formData['city'] ?? '',
        neighborhood: formData['neighborhood'] ?? '',
        address: formData['address'] ?? '',
        location: LatLng(
          formData['latitude'] ?? 0.0,
          formData['longitude'] ?? 0.0,
        ),
        area: (formData['area'] ?? 0).toDouble(),
        bedrooms: formData['bedrooms'] ?? 0,
        bathrooms: formData['bathrooms'] ?? 0,
        amenities: List<String>.from(formData['amenities'] ?? []),
        images: imageUrls,
        ownerId: user.uid,
        status: ListingStatus.active,
        views: 0,
        favoritesCount: 0,
        createdAt: DateTime.now(),
      );

      // Save to Firebase
      await ref.read(listingControllerProvider.notifier).createListing(listing);

      if (mounted) {
        Navigator.pop(context); // Fermer le loader
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(content: Text('Annonce créée avec succès!')),
        );
        context.go('/dashboard');
      }
    } catch (e) {
      if (mounted) {
        Navigator.pop(context);
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Erreur: $e')),
        );
      }
    }
  }

  @override
  Widget build(BuildContext context) {
    final currentStep = ref.watch(currentStepProvider);

    return Scaffold(
      appBar: AppBar(
        title: const Text('Nouvelle annonce'),
        leading: IconButton(
          icon: const Icon(Icons.close),
          onPressed: () => _showExitConfirmation(context),
        ),
      ),
      body: Column(
        children: [
          // Indicateur de progression
          _buildStepIndicator(currentStep),

          // Contenu de l'étape
          Expanded(
            child: PageView(
              controller: _pageController,
              physics: const NeverScrollableScrollPhysics(),
              onPageChanged: (index) {
                ref.read(currentStepProvider.notifier).state = index;
              },
              children: [
                _Step1BasicInfo(formKey: _formKeys[0]),
                _Step2Location(formKey: _formKeys[1]),
                _Step3Details(formKey: _formKeys[2]),
                _Step4Amenities(formKey: _formKeys[3]),
                _Step5Images(formKey: _formKeys[4]),
                _Step6Review(formKey: _formKeys[5]),
              ],
            ),
          ),

          // Boutons de navigation
          _buildNavigationButtons(currentStep),
        ],
      ),
    );
  }

  Widget _buildStepIndicator(int currentStep) {
    final steps = [
      'Infos de base',
      'Localisation',
      'Détails',
      'Commodités',
      'Photos',
      'Révision',
    ];

    return Container(
      padding: const EdgeInsets.all(16),
      child: Row(
        children: List.generate(6, (index) {
          final isActive = index == currentStep;
          final isCompleted = index < currentStep;

          return Expanded(
            child: Column(
              children: [
                Row(
                  children: [
                    if (index > 0)
                      Expanded(
                        child: Container(
                          height: 2,
                          color: isCompleted
                              ? AppColors.primary
                              : AppColors.grey300,
                        ),
                      ),
                    Container(
                      width: 32,
                      height: 32,
                      decoration: BoxDecoration(
                        shape: BoxShape.circle,
                        color: isCompleted || isActive
                            ? AppColors.primary
                            : AppColors.grey300,
                      ),
                      child: Center(
                        child: isCompleted
                            ? const Icon(Icons.check,
                                color: AppColors.white, size: 16)
                            : Text(
                                '${index + 1}',
                                style: TextStyle(
                                  color: isActive
                                      ? AppColors.white
                                      : AppColors.grey600,
                                  fontWeight: FontWeight.bold,
                                  fontSize: 12,
                                ),
                              ),
                      ),
                    ),
                    if (index < 5)
                      Expanded(
                        child: Container(
                          height: 2,
                          color: isCompleted
                              ? AppColors.primary
                              : AppColors.grey300,
                        ),
                      ),
                  ],
                ),
                const SizedBox(height: 4),
                Text(
                  steps[index],
                  style: TextStyle(
                    fontSize: 10,
                    color: isActive ? AppColors.primary : AppColors.grey600,
                    fontWeight: isActive ? FontWeight.bold : FontWeight.normal,
                  ),
                  textAlign: TextAlign.center,
                ),
              ],
            ),
          );
        }),
      ),
    );
  }

  Widget _buildNavigationButtons(int currentStep) {
    return Container(
      padding: const EdgeInsets.all(16),
      decoration: BoxDecoration(
        color: Theme.of(context).scaffoldBackgroundColor,
        boxShadow: [
          BoxShadow(
            color: Colors.black.withValues(alpha: 0.1),
            blurRadius: 8,
            offset: const Offset(0, -2),
          ),
        ],
      ),
      child: SafeArea(
        child: Row(
          children: [
            if (currentStep > 0)
              Expanded(
                child: OutlinedButton(
                  onPressed: _previousStep,
                  child: const Text('Précédent'),
                ),
              ),
            if (currentStep > 0) const SizedBox(width: 16),
            Expanded(
              child: ElevatedButton(
                onPressed: _nextStep,
                child: Text(currentStep < 5 ? 'Suivant' : 'Publier'),
              ),
            ),
          ],
        ),
      ),
    );
  }

  void _showExitConfirmation(BuildContext context) {
    showDialog(
      context: context,
      builder: (context) => AlertDialog(
        title: const Text('Quitter'),
        content: const Text(
            'Voulez-vous vraiment quitter ? Vos modifications seront perdues.'),
        actions: [
          TextButton(
            onPressed: () => Navigator.pop(context),
            child: const Text('Annuler'),
          ),
          TextButton(
            onPressed: () {
              Navigator.pop(context);
              context.pop();
            },
            style: TextButton.styleFrom(foregroundColor: AppColors.error),
            child: const Text('Quitter'),
          ),
        ],
      ),
    );
  }
}

