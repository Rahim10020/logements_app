  @override
  ConsumerState<_Step4Amenities> createState() => _Step4AmenitiesState();
}

class _Step4AmenitiesState extends ConsumerState<_Step4Amenities> {
  final Set<String> _selectedAmenities = {};

  @override
  Widget build(BuildContext context) {
    return Form(
      key: widget.formKey,
      child: ListView(
        padding: const EdgeInsets.all(16),
        children: [
          Text(
            'Commodités',
            style: Theme.of(context).textTheme.headlineSmall,
          ),
          const SizedBox(height: 8),
          Text(
            'Sélectionnez les équipements disponibles',
            style: Theme.of(context).textTheme.bodyMedium?.copyWith(
                  color: AppColors.grey600,
                ),
          ),
          const SizedBox(height: 24),

          // Grouper par catégorie
          ...AmenityCategory.values.map((category) {
            final amenities = Amenities.getByCategory(category);
            if (amenities.isEmpty) return const SizedBox.shrink();

            return Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Text(
                  category.displayName,
                  style: Theme.of(context).textTheme.titleMedium,
                ),
                const SizedBox(height: 12),
                Wrap(
                  spacing: 8,
                  runSpacing: 8,
                  children: amenities.map((amenity) {
                    final isSelected = _selectedAmenities.contains(amenity.id);
                    return FilterChip(
                      label: Text(amenity.name),
                      selected: isSelected,
                      onSelected: (selected) {
                        setState(() {
                          if (selected) {
                            _selectedAmenities.add(amenity.id);
                          } else {
                            _selectedAmenities.remove(amenity.id);
                          }
                        });
                        ref.read(listingFormProvider.notifier).update((state) {
                          return {
                            ...state,
                            'amenities': _selectedAmenities.toList()
                          };
                        });
                      },
                    );
                  }).toList(),
                ),
                const SizedBox(height: 24),
              ],
            );
          }),
        ],
      ),
    );
  }
}

/// Étape 5: Images
class _Step5Images extends ConsumerStatefulWidget {
  final GlobalKey<FormState> formKey;

  const _Step5Images({required this.formKey});

  @override
  ConsumerState<_Step5Images> createState() => _Step5ImagesState();
}

class _Step5ImagesState extends ConsumerState<_Step5Images> {
  final List<XFile> _images = [];
  final ImagePicker _picker = ImagePicker();

  Future<void> _pickImages() async {
    final images = await _picker.pickMultiImage();
    if (images.isNotEmpty) {
      setState(() {
        _images.addAll(images);
      });
      // Sauvegarder les chemins des images
      ref.read(listingFormProvider.notifier).update((state) {
        return {...state, 'images': _images.map((e) => e.path).toList()};
      });
    }
  }

  Future<void> _takePicture() async {
    final image = await _picker.pickImage(source: ImageSource.camera);
    if (image != null) {
      setState(() {
        _images.add(image);
      });
      ref.read(listingFormProvider.notifier).update((state) {
        return {...state, 'images': _images.map((e) => e.path).toList()};
      });
    }
  }

  void _removeImage(int index) {
    setState(() {
      _images.removeAt(index);
    });
    ref.read(listingFormProvider.notifier).update((state) {
      return {...state, 'images': _images.map((e) => e.path).toList()};
    });
  }

  @override
  Widget build(BuildContext context) {
    return Form(
      key: widget.formKey,
      child: ListView(
        padding: const EdgeInsets.all(16),
        children: [
          Text(
            'Photos du logement',
            style: Theme.of(context).textTheme.headlineSmall,
          ),
          const SizedBox(height: 8),
          Text(
            'Ajoutez au moins 3 photos de qualité',
            style: Theme.of(context).textTheme.bodyMedium?.copyWith(
                  color: AppColors.grey600,
                ),
          ),
          const SizedBox(height: 24),

          // Boutons d'ajout
          Row(
            children: [
              Expanded(
                child: OutlinedButton.icon(
                  icon: const Icon(Icons.photo_library),
                  label: const Text('Galerie'),
                  onPressed: _pickImages,
                ),
              ),
              const SizedBox(width: 16),
              Expanded(
                child: OutlinedButton.icon(
                  icon: const Icon(Icons.camera_alt),
                  label: const Text('Appareil photo'),
                  onPressed: _takePicture,
                ),
              ),
            ],
          ),
          const SizedBox(height: 24),

          // Grille d'images
          if (_images.isEmpty)
            Card(
              child: Padding(
                padding: const EdgeInsets.all(48),
                child: Column(
                  children: [
                    Icon(
                      Icons.add_photo_alternate,
                      size: 64,
                      color: Colors.grey[400],
                    ),
                    const SizedBox(height: 16),
                    Text(
                      'Aucune photo ajoutée',
                      style: TextStyle(color: Colors.grey[600]),
                    ),
                  ],
                ),
              ),
            )
          else
            GridView.builder(
              shrinkWrap: true,
              physics: const NeverScrollableScrollPhysics(),
              gridDelegate: const SliverGridDelegateWithFixedCrossAxisCount(
                crossAxisCount: 3,
                crossAxisSpacing: 8,
                mainAxisSpacing: 8,
              ),
              itemCount: _images.length,
              itemBuilder: (context, index) {
                return Stack(
                  children: [
                    ClipRRect(
                      borderRadius: BorderRadius.circular(8),
                      child: Image.network(
                        _images[index].path,
                        width: double.infinity,
                        height: double.infinity,
                        fit: BoxFit.cover,
                        errorBuilder: (_, __, ___) => Container(
                          color: AppColors.grey300,
                          child: const Icon(Icons.image),
                        ),
                      ),
                    ),
                    Positioned(
                      top: 4,
                      right: 4,
                      child: CircleAvatar(
                        radius: 16,
                        backgroundColor: AppColors.error,
                        child: IconButton(
                          padding: EdgeInsets.zero,
                          icon: const Icon(Icons.close,
                              size: 16, color: AppColors.white),
                          onPressed: () => _removeImage(index),
                        ),
                      ),
                    ),
                    if (index == 0)
                      Positioned(
                        bottom: 4,
                        left: 4,
                        child: Container(
                          padding: const EdgeInsets.symmetric(
                              horizontal: 8, vertical: 4),
                          decoration: BoxDecoration(
                            color: AppColors.primary,
                            borderRadius: BorderRadius.circular(4),
                          ),
                          child: const Text(
                            'Principale',
                            style:
                                TextStyle(color: AppColors.white, fontSize: 10),
                          ),
                        ),
                      ),
                  ],
                );
              },
            ),

          if (_images.isNotEmpty) ...[
            const SizedBox(height: 16),
            Text(
              '${_images.length} photo${_images.length > 1 ? 's' : ''} ajoutée${_images.length > 1 ? 's' : ''}',
              style: Theme.of(context).textTheme.bodySmall,
              textAlign: TextAlign.center,
            ),
            const SizedBox(height: 8),
            const Text(
              'La première photo sera utilisée comme image principale',
              style: TextStyle(fontSize: 12, color: AppColors.grey600),
              textAlign: TextAlign.center,
            ),
          ],
        ],
      ),
    );
  }
}

